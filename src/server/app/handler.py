    def _serve_share(self, token):
        path, error = _s.verify_share_token(token)
        if error:
            self._send_share_error(error)
            return
        target = (_s.CONFIG.content_root / path).resolve()
        try:
            target.relative_to(_s.CONFIG.content_root)
        except ValueError:
            self._send_share_error("invalid")
            return
        if not target.exists() or target.suffix.lower() not in (".md", ".html"):
            self._send_share_error("invalid")
            return
        # A shared .html is already a standalone page: served as-is (noindex),
        # no marked re-rendering — the owner's work, like a shared .md.
        if target.suffix.lower() == ".html":
            body = target.read_bytes()
            self.send_response(200)
            self.send_header("Content-Type", "text/html; charset=utf-8")
            self.send_header("Content-Length", str(len(body)))
            self.send_header("Cache-Control", "no-store")
            self.send_header("X-Robots-Tag", "noindex, nofollow")
            self.end_headers()
            self.wfile.write(body)
            return
        # Strip leading YAML frontmatter before embedding, like the viewer build:
        # left in, marked renders the `tags:` line + closing `---` as a setext H2
        # that leaks into the page and the table of contents.
        _, content = _s._import_build()._parse_frontmatter(
            target.read_text(encoding="utf-8"))
        title = target.name
        extensions_css, extensions_js = _s.share_extension_assets()
        body = _s.render_page("share",
            title=html.escape(title),
            # `</` → `<\/`: a doc containing "</script>" must not close the
            # <script> tag and inject HTML outside the sanitized content.
            content_json=json.dumps(content).replace("</", "<\\/"),
            # Extension assets so extension-generated content keeps its design
            # and interactions once shared.
            extensions_css=extensions_css,
            extensions_js=extensions_js,
            lang=html.escape(_s.CONFIG.lang, quote=True),
            footer_label=_s._t("share_footer").format(
                site_name=html.escape(_s.CONFIG.site_name, quote=True)),
            toc_title=_s._t("share_toc_title"),
        ).encode("utf-8")
        self.send_response(200)
        self.send_header("Content-Type", "text/html; charset=utf-8")
        self.send_header("Content-Length", str(len(body)))
        self.send_header("Cache-Control", "no-store")
        self.send_header("X-Robots-Tag", "noindex, nofollow")
        self.end_headers()
        self.wfile.write(body)

    def _bump_session_epoch(self, email: str) -> int:
        """Increments the account's session epoch → invalidates ALL issued
        cookies (the user's own AND any thief's). The read-increment-write isn't
        strictly atomic, but the monotonic increment suffices: two concurrent
        bumps invalidate the old sessions either way."""
        new_epoch = _s.current_session_epoch(email) + 1
        _s.get_store().upsert_user(email, {"session_epoch": new_epoch})
        return new_epoch

    # ── JSON helpers ─────────────────────────────────────────────────────────

    def _read_body(self) -> bytes:
        """Reads the request body, bounded to MAX_BODY_BYTES. Beyond the bound:
        return b"" and close the connection to discard the unread body (avoids
        memory blow-up). Callers treat b"" as an empty/invalid body."""
        length = _s._safe_int(self.headers.get("content-length"))
        if length > _s.MAX_BODY_BYTES:
            self.close_connection = True
            return b""
        return self.rfile.read(length) if length > 0 else b""

    def _read_json(self):
        body = self._read_body()
        if not body:
            return {}
        try:
            return json.loads(body)
        except (ValueError, json.JSONDecodeError):
            return {}

    def _accepts_gzip(self):
        return "gzip" in self.headers.get("Accept-Encoding", "").lower()

    def _send_bytes(self, status, body, content_type, extra_headers=None):
        """Sends bytes with gzip compression if the client supports it and body > 1KB."""
        headers = list(extra_headers or [])
        if self._accepts_gzip() and len(body) > 1024:
            body = gzip.compress(body, compresslevel=6)
            headers.append(("Content-Encoding", "gzip"))
        self.send_response(status)
        self.send_header("Content-Type", content_type)
        self.send_header("Content-Length", str(len(body)))
        for k, v in headers:
            self.send_header(k, v)
        self.end_headers()
        self.wfile.write(body)

    def _send_json(self, status, payload):
        body = json.dumps(payload, ensure_ascii=False).encode("utf-8")
        self._send_bytes(status, body, "application/json; charset=utf-8",
                         [("Cache-Control", "no-store")])

    def _serve_static_gzip(self, rel, content_type):
        """Serves a static dist/ file with gzip + ETag revalidation (304).

        SimpleHTTPRequestHandler doesn't compress; for the large prose JSON
        indices we go through _send_bytes (gzip >1KB). The ETag (mtime-size) lets
        the browser revalidate and get a 304 when nothing changed."""
        target = (_s.CONFIG.dist_dir / rel).resolve()
        try:
            target.relative_to(_s.CONFIG.dist_dir)
        except ValueError:
            self.send_error(404)
            return
        if not target.is_file():
            self.send_error(404)
            return
        st = target.stat()
        etag = f'"{int(st.st_mtime)}-{st.st_size}"'
        if self.headers.get("If-None-Match") == etag:
            self.send_response(304)
            self.send_header("ETag", etag)
            self.end_headers()
            return
        body = target.read_bytes()
        self._send_bytes(200, body, content_type,
                         [("ETag", etag), ("Cache-Control", "no-cache")])

    def _todo_index_from_path(self):
        m = re.match(r"^/api/todos/(\d+)$", self.path)
        return int(m.group(1)) if m else None

    # ── extensions ───────────────────────────────────────────────────────────

    def _dispatch_extension(self):
        """Route the request to a server extension, first matching route wins.
        Returns True if one handled it (response already sent, including 401/403
        from the role guard), else False (caller falls through). A raising
        handler doesn't kill the thread: generic 500 + stderr log."""
        path = self.path.split("?", 1)[0]
        for method, regex, handler, role in _s._CTX.extension_routes:
            if method != self.command:
                continue
            match = regex.match(path)
            if not match:
                continue
            if role == "admin" and not self._require_admin_or_403():
                return True
            if role == "auth" and not self._require_auth_or_401():
                return True
            try:
                handler(self, match)
            except Exception as e:
                print(f"[extensions] erreur handler {self.command} {path}: {e}",
                      file=sys.stderr)
                try:
                    self._send_json(500, {"error": "extension error"})
                except Exception:
                    pass  # response already partially sent by the handler
            return True
        return False

    # ── verbs ────────────────────────────────────────────────────────────────

    def do_HEAD(self):
        # No HEAD pipeline: the parent's default serves file metadata straight from
        # disk, bypassing the route tables, the session guard and the per-document
        # ACL (an existence oracle on private docs). The viewer never uses HEAD.
        self.send_error(405)

    def do_GET(self):
        if router.dispatch(self, routes_table.GET_ROUTES):
            return
        # Extension routes BEFORE the session guard — role applied per route.
        if self._dispatch_extension():
            return
        # First-boot (cloud, no admin): navigable pages redirect to /setup until
        # the initial admin exists. Placed after the explicit API/Bearer/MCP/share
        # routes so it doesn't hijack those independent auth channels or /api/me.
        if _s.setup_is_open():
            self.send_response(303)
            self.send_header("Location", "/setup")
            self.end_headers()
            return
        if not self._require_auth_or_redirect():
            return
        self._serve_navigable()

    def _serve_navigable(self):
        """Static / navigable tail of do_GET (after the explicit routes,
        extension dispatch, setup redirect and session guard): dotfile/source
        ACL, hidden-folder ACL, gzip dist indices, .html intercept, then the
        stdlib static handler."""
        rel = self.path.split("?", 1)[0].lstrip("/")
        # Security: the static handler serves any file under ROOT, dotfiles
        # included. Refuse dotfiles (.git/config holds the GitHub PAT) and source
        # code (*.py/*.pyc). Checks MUST run on the CANONICAL path (normalized +
        # URL-decoded exactly as translate_path resolves it): otherwise a
        # non-normalized path (/a//b, %2f tricks) slips past the literal-prefix
        # ACL here while translate_path still serves the file — a real ACL bypass.
        from urllib.parse import unquote as _unquote
        _norm = posixpath.normpath(_unquote(self.path.split("?", 1)[0]))
        _decoded = "/".join(w for w in _norm.split("/") if w and w not in (".", ".."))
        if (any(p.startswith(".") for p in _decoded.split("/"))
                or _decoded.endswith((".py", ".pyc"))):
            self.send_error(404)
            return
        # Per-document ACL (model B): a doc the viewer can't read is "not found"
        # (no-existence-oracle). The same posixpath-normalized form is checked, so
        # %2f / double-slash tricks can't slip a private doc past the gate.
        _vctx = self._viewer_ctx()
        if not _vctx.superuser and not _s.can_read(_decoded, _vctx):
            self.send_error(404)
            return
        # _backlinks.json: served gzip + ETag 304 (static handler doesn't compress).
        if rel == "_backlinks.json":
            self._serve_index_filtered(rel)
            return
        # _notes-index.json (dist/): counters for the tree's badges. Without this
        # route the static handler (content/) 404'd and the viewer fell back to {}.
        if rel == "_notes-index.json":
            self._serve_index_filtered(rel)
            return
        # _tasks-index.json — computed LIVE from current files (not the dist
        # snapshot) so ticking a checkbox reflects immediately. Filtered per-viewer.
        if rel == "_tasks-index.json":
            tasks = _s._live_tasks_index()
            ctx = self._viewer_ctx()
            if not ctx.superuser:
                tasks = [t for t in tasks if _s.can_read(t.get("path", ""), ctx)]
            self._send_json(200, tasks)
            return
        if not rel or rel.endswith("/"):
            rel = (rel + "index.html") if rel else "index.html"
        if rel.endswith(".html"):
            target = (_s.CONFIG.dist_dir / rel).resolve()
            try:
                target.relative_to(_s.CONFIG.dist_dir)
                if target.is_file():
                    body = target.read_bytes()
                    # no-store: the shell inlines the app JS, so a cached shell = stale
                    # JS; it must be revalidated every load for a deploy to land.
                    self._send_bytes(200, body, "text/html; charset=utf-8",
                                     [("Cache-Control", "no-store")])
                    return
            except (ValueError, OSError):
                pass
        return super().do_GET()

    def do_POST(self):
        if router.dispatch(self, routes_table.POST_ROUTES):
            return
        # Extension routes (POST): after native routes, before the 404. Role per
        # route, default "admin".
        if self._dispatch_extension():
            return
        self.send_response(404)
        self.end_headers()

    def do_PATCH(self):
        # Per-route guards (like do_POST): notes + share repoint keep ADMIN_CSRF,
        # todos is CSRF_BASE (a member patches its OWN list). No blanket admin.
        if router.dispatch(self, routes_table.PATCH_ROUTES):
            return
        self.send_response(404)
        self.end_headers()

    def do_DELETE(self):
        # Per-route guards (like do_POST): admin routes carry ADMIN_CSRF, content
        # routes their own Guard (/api/file is CSRF_BASE → authorized per-document
        # in docs.delete). No blanket admin, so a member can delete a doc they own.
        if router.dispatch(self, routes_table.DELETE_ROUTES):
            return
        self.send_response(404)
        self.end_headers()

    def do_PUT(self):
        # Per-route guards (like do_POST): /api/file is CSRF_BASE → authorized
        # per-document in docs.file_put (create/edit ladder). No blanket admin.
        if router.dispatch(self, routes_table.PUT_ROUTES):
            return
        self.send_response(404)
        self.end_headers()

    def log_message(self, fmt, *args):
        if _s.CONFIG.auth_enabled:
            # Mask the MCP token in the path (/mcp/<token>) so it is not written
            # in clear text in the Fly logs.
            msg = re.sub(r"/mcp/[^/\s\"?]+", "/mcp/***", fmt % args)
            sys.stderr.write("%s - %s\n" % (self.address_string(), msg))
            sys.stderr.flush()
