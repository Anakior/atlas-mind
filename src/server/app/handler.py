"""The HTTP request Handler: plumbing (cookies, sessions, CSRF, gzip, static serve)
plus the auth guard methods the router calls. Route bodies live in server/routes/*.py;
this class only carries the request machinery + guards + the do_<VERB> dispatchers.
Module-level helpers and config are reached through the `server` facade (`_s`)."""
from http.server import SimpleHTTPRequestHandler
import server as _s
from server.app import router, routes_table
import gzip
import html
import json
import os
import posixpath
import re
import sys


class Handler(SimpleHTTPRequestHandler):
    # Explicit MIME types for /vendor/: the system mimetypes table can miss
    # woff2 on some distros, and the browser then rejects mistyped fonts/CSS.
    extensions_map = {
        **SimpleHTTPRequestHandler.extensions_map,
        ".js": "text/javascript; charset=utf-8",
        ".css": "text/css; charset=utf-8",
        ".woff2": "font/woff2",
    }

    def __init__(self, *args, **kwargs):
        # Static handler serves content/ (.md); dist/ indices are intercepted
        # before it, PWA assets (web/) redirected by translate_path below.
        super().__init__(*args, directory=str(_s.CONFIG.content_root), **kwargs)

    def translate_path(self, path):
        clean = path.split("?", 1)[0].split("#", 1)[0]
        if clean == "/manifest.json":
            # PWA manifest is generated by build.py into dist/, not static in web/.
            return str(_s.CONFIG.dist_dir / "manifest.json")
        if clean in ("/icon.svg", "/favicon.ico", "/sw.js"):
            return str(_s.CONFIG.web_dir / clean.lstrip("/"))
        if clean.startswith("/vendor/"):
            # Open prefix → anti-traversal: after normpath a path escaping
            # /vendor/ (/vendor/../src/server.py) no longer starts with it → 404.
            from urllib.parse import unquote as _unquote
            normalized = posixpath.normpath(_unquote(clean))
            if normalized.startswith("/vendor/"):
                target = _s.CONFIG.web_dir / normalized.lstrip("/")
                if target.is_dir():
                    # Refuse directories: a trailing slash would otherwise render
                    # an HTML autoindex. Only FILES are served under /vendor/.
                    return str(_s.CONFIG.web_dir / "vendor" / "__refused__")
                return str(target)
            return str(_s.CONFIG.web_dir / "vendor" / "__refused__")
        return super().translate_path(path)

    def end_headers(self):
        # Security headers on ALL responses, centralized here. `no-referrer`
        # prevents a /share link token leaking via Referer to an external link
        # clicked from the doc. HSTS only in cloud (HTTPS).
        self.send_header("X-Content-Type-Options", "nosniff")
        # SAMEORIGIN (not DENY): the viewer renders .html docs in a same-origin
        # iframe (standalone deck/dashboard) which DENY would break; SAMEORIGIN
        # still blocks third-party clickjacking. Iframe stays sandbox=allow-scripts.
        self.send_header("X-Frame-Options", "SAMEORIGIN")
        self.send_header("Referrer-Policy", "no-referrer")
        if _s.CONFIG.auth_enabled:
            self.send_header("Strict-Transport-Security",
                             "max-age=63072000; includeSubDomains")
        super().end_headers()

    # ── auth ─────────────────────────────────────────────────────────────────

    def _session(self):
        """Returns {'email','role'} or None. In local mode, fakes an admin session."""
        if not _s.CONFIG.auth_enabled:
            return {"email": "local", "role": "admin"}
        cookie_header = self.headers.get("Cookie", "")
        for part in cookie_header.split(";"):
            part = part.strip()
            if part.startswith(_s.COOKIE_NAME + "="):
                return _s.verify_token(part[len(_s.COOKIE_NAME) + 1:])
        return None

    def _hidden_folders(self):
        """Folders (prefixes relative to content/) forbidden for the current
        viewer. Admin / local mode / no-auth → []. The 'api' role doesn't use
        cookie sessions, so it never reaches here."""
        sess = self._session()
        if not sess or sess.get("role") == "admin":
            return []
        user = _s.get_store().get_user_by_email(sess["email"])
        folders = (user or {}).get("hidden_folders") or []
        return [f.strip("/") for f in folders if isinstance(f, str) and f.strip("/")]

    def _viewer_ctx(self):
        """Per-request ACL context (model B). No session (local mode / no-auth) →
        superuser bypass (single operator on their own machine). A logged-in
        account (admin or viewer) → subject to the per-document ACL; the admin
        additionally curates the commons but does NOT see another user's private
        doc. The 'api' role never reaches here (no cookie session)."""
        sess = self._session()
        # Local mode (auth disabled) = single operator on their own machine →
        # superuser, regardless of the simulated admin session.
        if not _s.CONFIG.auth_enabled:
            return _s.ViewerCtx(set(), True, None, superuser=True)
        # Cloud + no session → anonymous, NOT superuser (fail closed). Content
        # routes are AUTH-guarded so this is defensive, but it must never grant a
        # full bypass to an unauthenticated caller.
        if not sess:
            return _s.viewer_ctx(None)
        return _s.viewer_ctx(sess)

    def _serve_index_filtered(self, rel):
        """Serve _backlinks.json / _notes-index.json with docs the current viewer
        can't read REMOVED (else the Mind/backlinks leak their names). Admin (no
        restriction) → fast static gzip path."""
        ctx = self._viewer_ctx()
        if ctx.superuser:
            self._serve_static_gzip(rel, "application/json; charset=utf-8")
            return
        try:
            data = json.loads((_s.CONFIG.dist_dir / rel).read_text(encoding="utf-8"))
        except (OSError, ValueError):
            self.send_error(404)
            return
        if isinstance(data, list):  # _tasks-index.json: [{path, line, text, done}]
            self._send_json(200, [
                it for it in data
                if isinstance(it, dict) and _s.can_read(it.get("path", ""), ctx)])
            return
        out = {}
        for path, val in data.items():
            if not _s.can_read(path, ctx):
                continue
            if isinstance(val, dict):  # _backlinks.json: {path: {in:[...], out:[...]}}
                val = {k: ([x for x in v if _s.can_read(x, ctx)]
                           if isinstance(v, list) else v)
                       for k, v in val.items()}
            out[path] = val
        self._send_json(200, out)

    def _is_authed(self):
        return self._session() is not None

    def _is_admin(self):
        sess = self._session()
        return sess is not None and sess.get("role") == "admin"

    def _require_auth_or_redirect(self):
        if self._is_authed():
            return True
        self.send_response(303)
        self.send_header("Location", "/login")
        self.end_headers()
        return False

    def _require_auth_or_401(self):
        if self._is_authed():
            return True
        self._send_json(401, {"error": "unauthorized"})
        return False

    def _require_admin_or_403(self):
        if not self._is_authed():
            self._send_json(401, {"error": "unauthorized"})
            return False
        if not self._is_admin():
            self._send_json(403, {"error": "forbidden"})
            return False
        return True

    def _request_origin_host(self):
        """Host of the request's origin (Origin, else Referer), or None if
        neither header is present. Used for the same-origin CSRF check."""
        from urllib.parse import urlsplit
        for header in ("Origin", "Referer"):
            value = self.headers.get(header)
            if value:
                # Origin "null" (sandbox/file://): no usable host → cross-origin.
                if value == "null":
                    return ""
                return urlsplit(value).netloc
        return None

    def _check_csrf_base_or_403(self):
        """Base CSRF defense (JSON Content-Type + same-origin). Defense-in-depth
        AND the only defense enforceable BEFORE a session exists (first-admin
        setup, otherwise protected by the constant-time setup-token). Local mode
        (auth disabled) stays open."""
        if not _s.CONFIG.auth_enabled:
            return True
        ctype = self.headers.get("Content-Type", "")
        content_length = int(self.headers.get("Content-Length") or 0)
        # Content-Type is only enforceable with a BODY: a bodyless mutating
        # request (DELETE /api/share/<id>, user delete) legitimately has none —
        # its CSRF defense then rests on same-origin + the X-CSRF-Token below.
        # Without this nuance every bodyless DELETE 415'd (share revoke broken).
        if content_length > 0 and "application/json" not in ctype.lower():
            self._send_json(415, {"error": "Content-Type must be application/json"})
            return False
        origin_host = self._request_origin_host()
        if origin_host is not None and origin_host != self.headers.get("Host", ""):
            self._send_json(403, {"error": "cross-origin request refused"})
            return False
        return True

    def _check_csrf_or_403(self):
        """Full CSRF defense for authenticated mutating requests, in order:
          1. application/json Content-Type required — a cross-site <form> can't
             emit it without a CORS preflight (blocked: no third-party origin).
          2. Same-origin Origin/Referer when present (else 403).
          3. Synchronizer X-CSRF-Token bound to the session, HMAC(secret,
             email|epoch): a third-party page can neither read the kb_csrf cookie
             (Same-Origin Policy) nor forge the HMAC. The strong barrier.

        Local mode (auth disabled) stays open: single-user on 127.0.0.1."""
        if not _s.CONFIG.auth_enabled:
            return True
        if not self._check_csrf_base_or_403():
            return False
        sess = self._session()
        if sess is None:
            self._send_json(401, {"error": "unauthorized"})
            return False
        epoch = _s.current_session_epoch(sess.get("email"))
        provided = self.headers.get("X-CSRF-Token", "")
        if not _s.verify_csrf_token(sess.get("email"), epoch, provided):
            self._send_json(403, {"error": "missing or invalid CSRF token"})
            return False
        return True

    def _require_api_bearer(self):
        """Verifies the Authorization: Bearer header + rate limit.

        Returns the user dict on success, or sends an error and returns None.
        Logs every successful call to stderr for audit.
        """
        sess = _s.verify_api_bearer(self.headers.get("Authorization", ""))
        if not sess:
            self._send_json(401, {"error": "invalid or missing bearer token"})
            return None
        auth = self.headers.get("Authorization", "")
        token = auth.strip().split(None, 1)[1] if " " in auth else ""
        if not _s.api_rate_limit_ok(_s._hash_api_token(token)):
            self._send_json(429, {"error": "rate limit exceeded (120/min)"})
            return None
        sys.stderr.write(f"[api] {self.command} {self.path} email={sess['email']}\n")
        sys.stderr.flush()
        return sess

