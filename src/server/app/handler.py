    def _client_ip(self) -> str:
        """Real client IP behind a reverse proxy.

        If server.trusted_ip_header is configured, that header is authoritative
        — the LAST element if it carries a list: with an appending proxy (nginx
        `$proxy_add_x_forwarded_for`) only the last element is set by the trusted
        proxy; the left ones are client-supplied and forgeable (an attacker
        could rotate them to bypass the login rate limit)."""
        trusted_header = _s.CONFIG.trusted_ip_header
        if trusted_header:
            value = self.headers.get(trusted_header, "").split(",")[-1].strip()
            return value or self.client_address[0]
        # No trusted header: honor Fly-Client-IP only when actually on Fly
        # (platform-injected at the edge). We do NOT trust X-Forwarded-For here —
        # its leftmost element is client-forgeable. Self-hosters behind a proxy
        # must set server.trusted_ip_header.
        if os.environ.get("FLY_APP_NAME") or os.environ.get("FLY_ALLOC_ID"):
            fly_ip = self.headers.get("Fly-Client-IP", "").strip()
            if fly_ip:
                return fly_ip
        return self.client_address[0]

    def _session_cookie_pair(self, email: str, role: str, epoch: int):
        """The TWO Set-Cookie headers of an open session: the signed session
        cookie (HttpOnly) and the readable CSRF cookie (NOT HttpOnly, read by the
        page to set X-CSRF-Token). Same Max-Age, same Secure policy."""
        secure = "; Secure" if _s.CONFIG.auth_enabled else ""
        max_age = _s.CONFIG.session_max_age
        session_token = _s.make_token(email, role, epoch)
        csrf_token = _s.make_csrf_token(email, epoch)
        return [
            (f"{_s.COOKIE_NAME}={session_token}; Path=/; HttpOnly; "
             f"SameSite=Lax; Max-Age={max_age}{secure}"),
            # NOT HttpOnly on purpose: the page must read it to replay as a
            # header. Carries no secret (derived HMAC) and stealing it is not
            # enough — the HttpOnly session cookie is also required.
            (f"{_s.CSRF_COOKIE_NAME}={csrf_token}; Path=/; "
             f"SameSite=Lax; Max-Age={max_age}{secure}"),
        ]

    def _open_session_response(self, email: str, role: str):
        """303 to / with the session+CSRF cookies set (form login)."""
        epoch = _s.current_session_epoch(email)
        self.send_response(303)
        self.send_header("Location", "/")
        for cookie in self._session_cookie_pair(email, role, epoch):
            self.send_header("Set-Cookie", cookie)
        self.end_headers()

    def _verify_second_factor(self, user: dict, totp_code: str,
                              recovery_code: str) -> bool:
        """Validate the 2nd factor: TOTP code (constant time) OR single-use
        recovery code (consumed). The recovery code is only attempted if no TOTP
        code is provided, to avoid consuming a code by mistake."""
        if totp_code:
            step = _s.verify_totp_step(user.get("totp_secret") or "", totp_code)
            if step is None:
                return False
            # Anti-replay: refuse reusing a step already accepted (proxy-phishing
            # / shoulder-surf, a code is valid ~90s). Fail-open on a store hiccup
            # — never lock out a legitimate login over a hardening control.
            email = user.get("email") or ""
            if step <= _s.get_store().get_last_totp_step(email):
                return False
            _s.get_store().set_last_totp_step(email, step)
            return True
        if recovery_code:
            return _s.consume_recovery_code(user.get("email"), recovery_code)
        return False

