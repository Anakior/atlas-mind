function t(key, ...args) {
  const dict = STRINGS[LANG] || STRINGS.fr;
  let entry = dict[key];

  if (entry === undefined) entry = STRINGS.fr[key];

  if (entry === undefined) return key;

  return typeof entry === 'function' ? entry(...args) : entry;
}

