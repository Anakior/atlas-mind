function relativeDate(epoch) {
  if (!epoch) return '';
  const diff = Date.now() / 1000 - epoch;

  if (diff < 60) return t('justNow');

  if (diff < 3600) return t('minAgo', Math.floor(diff / 60));

  if (diff < 86400) return t('hoursAgo', Math.floor(diff / 3600));

  if (diff < 86400 * 7) return t('daysAgo', Math.floor(diff / 86400));

  return new Date(epoch * 1000).toLocaleDateString(LANG, {
    day: 'numeric',
    month: 'short',
    year: 'numeric',
  });
}

function slugify(s) {
  return s
    .toLowerCase()
    .normalize('NFD')
    .replace(/[̀-ͯ]/g, '')
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-+|-+$/g, '');
}

function escapeHtml(s) {
  return String(s).replace(
    /[&<>"']/g,
    (c) => ({ '&': '&amp;', '<': '&lt;', '>': '&gt;', '"': '&quot;', "'": '&#39;' })[c],
  );
}

let mdCount = 0,
  otherCount = 0;
const fileMap = {};

function index(node) {
  for (const c of node.children || []) {
    if (c.type === 'file') {
      fileMap[c.path] = c;

      if (c.ext === '.md') mdCount++;
      else otherCount++;
    } else index(c);
  }
}

